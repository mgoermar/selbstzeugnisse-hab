    <h1>Selbstzeugnis-Forschungsportal</h1>

    <p>Im Rahmen des vom Niedersächsischen Ministerium für Wissenschaft und Kultur (PRO*Niedersachsen) von 2014 bis 2017 geförderten Projektes „Selbstzeugnisse der Frühen Neuzeit in der Herzog August Bibliothek. Digitale Edition des Diariums von Herzog August dem Jüngeren, Selbstzeugnis-Repertorium und Forschungsportal“ wurden die handschriftlichen Selbstzeugnisse der Herzog August Bibliothek (HAB) unter Zuhilfenahme der gedruckt vorliegenden Handschriftenkataloge identifiziert, autoptisch untersucht und in einem digitalen Repertorium systematisch verzeichnet. Parallel wurde exemplarisch eine digitale Edition des den Zeitraum von 1594 bis 1635 umfassenden Tagebuches von August dem Jüngeren, Herzog zu Braunschweig-Lüneburg (1579-1666), erstellt.</p>
    
    <p>Im Anschluss an das Repertorium und die Edition des Tagebuchs von August dem Jüngeren führt die HAB seit Anfang 2019 das ebenfalls im Programm PRO*Niedersachsen vom Niedersächsischen Ministerium für Wissenschaft und Kultur geförderte Projekt "Selbstzeugnisse der Frühen Neuzeit in der Herzog August Bibliothek II: Digitale Edition der Tagebücher von Herzog Ludwig Rudolph und Herzogin Christine Luise von Braunschweig-Wolfenbüttel" durch. Die in diesem Rahmen erarbeitete historisch-kritisch kommentierte Edition von vier Tagebüchern des herzoglichen Paares aus der Zeit zwischen 1698 und 1724 wird im Rahmen des bestehenden Portals sukzessive digital veröffentlicht. Sie wird vervollständigt durch weiterführende und begleitende Texte zu den Quellen, zu den beiden Tagebuchschreibenden und ihrem kulturgeschichtlichen Hintergrund. Zudem bietet das Portal in einer Gesamtbibliographie diesbezügliche (und großteils digitalisierte) Quellen aus den Beständen der HAB sowie einschlägige Literatur zum Herzogspaar, zum Territorium, zum Hof und seiner Kultur sowie zur Selbstzeugnisforschung.</p>

    <p>Vorbehaltlich künftiger Förderung ist beabsichtigt, diese Editions- und Forschungslinie der Bibliothek auch nach Abschluss der Selbstzeugnisse II mit einem weiteren Folgevorhaben fortzuführen.</p>

